let i = 1, y = 3;
console.log(i - y);

// String merging with +
let s = 'my' + 'string';
console.log(s);

// Note, iff any of the operands is a string, then other one will be converted to a string too.
console.log('1' + 20);
console.log('2'+ 1000);
